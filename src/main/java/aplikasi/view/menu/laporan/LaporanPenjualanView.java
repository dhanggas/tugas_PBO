    private void refreshDataPenjualan(Date tglAwal, Date tglAkhir) {
        try {
            tablePenjualanController.clearData();
            daftarPenjualan = repoPenjualan.findPenjualanByTglBetween(tglAwal, tglAkhir);
            for (Penjualan penjualan : daftarPenjualan) {
                Object[] row = {
                    penjualan.getKode(),
                    ValueFormatterFactory.getLocalDateLong(penjualan.getTgl().toLocalDate())
                };
                tablePenjualanController.getModel().addRow(row);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Tidak dapat menampilkan data penjualan", getTitle(), JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(LaporanPenjualanView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void setFields(Penjualan jual) {
        try {
            txtKodePenjualan.setText(jual.getKode());

            Pelanggan p = jual.getPelanggan();
            txtNamaPelanggan.setText(p.getNama());
            txtTelpPelanggan.setText(p.getTlp());
            txtTanggalPenjualan.setText(ValueFormatterFactory.getLocalDateLong(jual.getTgl().toLocalDate()));

            daftarDetailPenajualan = repoPenjualan.findPenjualanDetailByPenjualanKode(jual.getKode());
            tableBarangPenjualanController.clearData();
            for (PenjualanDetail detailJual : daftarDetailPenajualan) {
                Barang barang = detailJual.getBarang();
                KategoriBarang kategori = barang.getKategori();
                Object[] row = {
                    barang.getKode(),
                    barang.getNama(),
                    kategori.getNama(),
                    detailJual.getJumlah()
                };
                tableBarangPenjualanController.getModel().addRow(row);
            }
        } catch (SQLException ex) {
            Logger.getLogger(LaporanPenjualanView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void clearFields() {
        txtKodePenjualan.setText("");
        txtNamaPelanggan.setText("");
        txtTelpPelanggan.setText("");
        txtTanggalPenjualan.setText("");
        daftarDetailPenajualan.clear();
        tableBarangPenjualanController.clearData();
    }

